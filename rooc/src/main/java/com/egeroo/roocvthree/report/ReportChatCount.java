package com.egeroo.roocvthree.report;

import java.util.Date;



public class ReportChatCount {
	
	private int weeknumday;
	private int chatcount;
	private int chatgrowth;
	private String branch_name;
	private String kcp_name;
	private Date period;
	private Date createdtime;
	private String channel;
	private String channelparam;
	
	public int getWeeknumday() {
		return weeknumday;
	}
	public void setWeeknumday(int weeknumday) {
		this.weeknumday = weeknumday;
	}
	public int getChatcount() {
		return chatcount;
	}
	public void setChatcount(int chatcount) {
		this.chatcount = chatcount;
	}
	public int getChatgrowth() {
		return chatgrowth;
	}
	public void setChatgrowth(int chatgrowth) {
		this.chatgrowth = chatgrowth;
	}
	public String getBranch_name() {
		return branch_name;
	}
	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}
	public String getKcp_name() {
		return kcp_name;
	}
	public void setKcp_name(String kcp_name) {
		this.kcp_name = kcp_name;
	}
	public Date getPeriod() {
		return period;
	}
	public void setPeriod(Date period) {
		this.period = period;
	}
	public Date getCreatedtime() {
		return createdtime;
	}
	public void setCreatedtime(Date createdtime) {
		this.createdtime = createdtime;
	}
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public String getChannelparam() {
		return channelparam;
	}
	public void setChannelparam(String channelparam) {
		this.channelparam = channelparam;
	}
	
	
	
	

}
